#include "libft.h"

int	ft_isalnum(int arg)
{
	if (arg >= 48 && arg <= 57)
		return (1);
	if (arg >= 65 && arg <= 90)
		return (1);
	if (arg >= 97 && arg <= 122)
		return (1);
	return (0);
}
